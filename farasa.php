            <h2><a href="http://farasa.net/home/?p=1211" rel="bookmark" title="لینک ثابت به طراحی و عرضه لوح فشرده و دوره آنلاین چند رسانه ای فعالیت بدنی و تناسب اندام">طراحی و عرضه لوح فشرده و دوره آنلاین چند رسانه ای فعالیت بدنی و تناسب اندام</a></h2>

                <p>لوح فشرده و دوره آنلاین چندرسانه ای فعالیت بدنی و تناسب اندام به همت موسسه توسعه دانش، پژوهش و فناوری فرزان عرضه شد.</p>
<p><a rel="attachment wp-att-1216" href="http://farasa.net/home/?attachment_id=1216"><img class="alignright size-full wp-image-1216" title="فعالیت-بدنی-و-تناسب-اندام" src="http://farasa.net/home/wp-content/uploads/2018/06/فعالیت-بدنی-و-تناسب-اندام-768x434.png" alt="فعالیت-بدنی-و-تناسب-اندام" width="500" height="300"></a></p>
<p>&nbsp;</p>
<p style="text-align: justify;">این دوره، برای کمک به افراد بزرگسال ایرانی در جهت رسیدن به سطح فعالیت بدنی و آمادگی جسمانی مطلوب و تناسب اندام طراحی شده است. گروه هدف اصلی در این دوره، افرادی است که سطح مطلوب فعالیت بدنی شامل ۱۵۰ دقیقه فعالیت بدنی با شدت متوسط و یا ۷۵ دقیقه فعالیت شدید در هفته در طی ۳ تا ۵ روز در هفته را ندارند و به دنبال راهی برای ارتقای سطح فعالیت بدنی و بهره مندی از فواید آن هستند.</p>
<p style="text-align: justify;" dir="rtl">در این دوره، روشهای ساده ای برای ارزیابی برخی شاخص های آمادگی جسمانی ارایه شده که کاربر می تواند در محل کار یا &nbsp;منزل آنها را انجام دهد و از وضعیت خود آگاه شود. بر اساس وضعیت فرد، برای وی توصیه ها و حرکات ورزشی در نظر گرفته شده است.</p>
<p style="text-align: justify;">شرکت در دوره حاضر، می تواند باعث بهبود میزان فعالیت بدنی و تناسب اندام در فرد شود.</p>
<p><strong>در کنار این دوره کمپین هوشمند فعالیت بدنی نیز برگزار می شود. </strong></p>
<p style="text-align: justify;">کمپین هوشمند فعالیت بدنی شامل یک برنامه سه ماهه فعالیت بدنی برای کارکنان و سرویس های هوشمند با امکان تمدید زمان است و دربرگیرنده مواردی همچون ارائه برنامه برای فعالیت بدنی روزانه کارکنان و خانواده های آنها متناسب با ویژگی های فردی، ارزیابی وضعیت جسمانی و میزان آمادگی قلبی – تنفسی کارکنان سازمان در یک سمینار حضوری، معرفی به باشگاه های تناسب اندام و ورزشی (با معرفی نامه رسمی و پشتیبانی دستگاه و خدمات رایگان یا با تخفیف های ویژه و خدمات VIP)، آموزش مجازی فعالیت بدنی (دارای امتیاز ضمن خدمت برای کارکنان دولت)، وبینار آموزشی فعالیت بدنی (بویژه برای دستگاه هایی که شعب آنها در سطح کشور پراکنده است)، ارزیابی و بازخوردها و توصیه های هوشمند فعالیت بدنی و سلامتی به کارکنان، پیام های عمومی و هوشمند روزانه (شامل پیام های آموزشی، انگیزشی و یادآور)، مشاوره (متناسب با نیاز کارکنان) و برنامه ها و تورهای ورزشی و تمرینی می باشد.</p>
<p>در کنار لوح فشرده فعالیت بدنی و تناسب اندام، امکان برگزاری دوره آنلاین و چند رسانه ای فعالیت بدنی نیز برای کارکنان سازمان ها (با تاییدیه و مجوز از سازمان برنامه و بودجه) فراهم شده است. این دوره که بصورت آنلاین برگزار می شود دارای امتیاز آموزش ضمن خدمت برای کارکنان دولت است.</p>
<p style="text-align: justify;">سازمان ها اعم از دستگاه های دولتی یا خصوصی و نهادهای عمومی می توانند برای استفاده از این برنامه آموزشی با ما <a rel="noopener" href="http://farasa.net/home/?page_id=7" target="_blank">تماس</a> بگیرند.</p>
