            <h2><a href="http://farasa.net/home/?p=1228" rel="bookmark" title="لینک ثابت به خرید لوح فشرده چندرسانه ای فعالیت بدنی و تناسب اندام توسط دانشگاه علوم پزشکی قزوین">خرید لوح فشرده چندرسانه ای فعالیت بدنی و تناسب اندام توسط دانشگاه علوم پزشکی قزوین</a></h2>

                <p><strong>دانشگاه علوم پزشکی قزوین</strong> لوح فشرده فعالیت بدنی و تناسب اندام را برای استفاده عموم مردم سفارش داد.</p>
<p style="text-align: justify;">تغییر سبک زندگی باعث شده است اکثریت مردم فاقد میزان لازم فعالیت بدنی روزانه باشند و این امر، یکی از عوامل افزایش بروز بیماری های مزمن از جمله افزایش وزن و چاقی، بیماری قلبی عروقی، دیابت، سندرم متابولیک و … شده است. لذا در راستای کاهش بیماری های غیرواگیر و مزمن، ارتقای فعالیت بدنی به لحاظ کیفی و کمی، جزء اهداف مهم نظام بهداشتی درمانی کشور می باشد.</p>
<p style="text-align: justify;">لوح فشرده آموزش چندرسانه ای فعالیت بدنی و تناسب اندام به همت موسسه توسعه دانش، پژوهش و فناوری فرزان تهیه و برای استفاده عموم مردم عرضه شده است.</p>
<p style="text-align: justify;">سرفصلهای اصلی دوره شامل آمادگی جسمانی مطلوب، ضرورت فعالیت بدنی مطلوب و عوارض کم تحرکی از جمله در کرکنان اداری و مدیران، پشت میزنشینی و عدم رعایت ارگونومی، هرم فعالیت بدنی، فعالیت های استقامتی، تمرینات کششی و انواع آن، قدرت عضلانی و تمرینات قدرتی و انواع تمرینات مقاومتی می باشند.</p>
<p style="text-align: justify;">همچنین فعالیت بدنی در بزرگسالان از جمله فعالیت بدنی در منزل، فعالیت بدنی هنگام تماشای تلویزیون، فعالیت بدنی در خارج از منزل و همچنین فعالیت بدنی در کودکان به تفکیک برای کودکان زیر۱ سال، کودکان ۱ تا ۳ سال و کودکان ۳ تا ۵ سال و فعالیت بدنی در سالمندان شامل تمرینات هوازی، تمرینات مقاومتی، رهنمودهای کلیدی و دستورالعمل های ویژه سالمندان&nbsp; و حفظ تعادل افراد مسن در خطر سقوط در زمره موضوعات این دوره می باشند.</p>
<p style="text-align: justify;">محتوای چندرسانه ای آموزش فعالیت بدنی و تناسب اندام، به شکل دوره آنلاین نیز عرضه شده است و از طریق “سامانه فراسا: آموزش و سنجش الکترونیک” به آدرس <a rel="noopener" href="http://www.farasa.net" target="_blank">www.farasa.net</a> قابل دسترسی و استفاده است.</p>
<p style="text-align: justify;">دوره آنلاین و چند رسانه ای فعالیت بدنی برای کارکنان سازمان ها از جمله کارکنان دستگاه های دولتیبرگزار می شود (با تاییدیه و مجوز از سازمان برنامه و بودجه). این دوره دارای امتیاز آموزش ضمن خدمت برای کارکنان دولت است.</p>
<p style="text-align: justify;">برنامه آموزشی فعالیت بدنی و تناسب اندام دارای تاییدیه وزارت بهداشت نیز می باشد. همچنین موسسه توسعه دانش، پژوهش و فناوری فرزان دارای مجوز نشر دیجیتال از وزارت فرهنگ و ارشاد اسلامی می باشد.</p>
<p style="text-align: justify;">دانشگاه های علوم پزشکی و سازمان ها اعم از دستگاه های دولتی، خصوصی و نهادهای عمومی می توانند برای استفاده از این برنامه آموزشی با ما <a rel="noopener" href="http://farasa.net/home/?page_id=7" target="_blank">تماس</a> بگیرند.</p>
