<p><strong> روش فعال سازی Javascript<br />
</strong><br />
برای نمایش فلش های آموزشی در دوره ها باید قابلیت Javascript در مرورگرتان فعال باشد.<br />
این قابلیت در بیشتر موارد، به صورت پیش فرض فعال است. اما ممکن است به دلیل برخی خطاهای سهوی، فعالیت ویروس‌ها و یا حتی تشخیص خود کاربر برای استفاده نکردن از Javascript این قابلیت در مرورگر غیرفعال شود.<br />
در این مطلب روش فعال کردن Javascript  به صورت قدم به قدم با توجه به نوع مرورگری که شما استفاده می‌کنید،شرح داده شده است.<br />
<strong><br />
۱) فعال کردن Javascript در Internet Explorer<br />
</strong><br />
1-  در منوی Tools گزینه‌ی Internet Options را کلیک کنید.<br />
۲-  در پنجره‌ی باز شده، به سربرگ Security مراجعه کنید.<br />
۳-  در فرم موجود در این سربرگ، گزینه Internet (با علامت کره زمین) را کلیک نمایید تا فعال شود، سپس در پایین این پنجره بر روی کلید Custom Level کلیک کنید.<br />
۴-  در لیست موجود در پنجره جدید باز شده (Security Settings) گزینه Scripting را بیابید. در زیر مجموعه این گزینه، گزینه‌ی Active Scripting را بیابید و مقدار آن را برابر Enable قرار دهید.<br />
۵-  ابتدا در پنجره Security Settings و سپس در Internet Options کلید OK را کلیک کنید.<br />
۶-  از مرورگر خود خود خارج شوید و مجددا آن را اجرا کنید تا تغییرات اعمال شود.</p>
<p><strong><br />
۲) فعال کردن Javascript در تمام نسخه‌های Firefox<br />
</strong><br />
1-  در منوی Tools گزینه Options را کلیک کنید.<br />
۲-  در پنجره باز شده به سربرگ Content بروید.<br />
۳-  در این سربرگ گزینه Enable Javascript را بیابید و اگر سمت چپ آن تیک نخورده، آن را تیک بزنید.<br />
۴-  بر روی کلیک OK در پایین پنجره کلیک کنید تا تغییرات ثبت شود.<br />
۵-  نیازی به خروج و را‌ه‌اندازی مجدد Firefox نیست.</p>
<p><strong><br />
۳) فعال کردن Javascript در Google Chrome<br />
</strong><br />
1-  جاوااسکریپت در مرورگر کروم به صورت ذاتی فعال است و فعال یا غیرفعال کردن آن از طریق تنظیمات مرورگر انجام نمی‌شود.</p>
<p><strong><br />
۴) فعال کردن Javascript در Opera<br />
</strong><br />
1-  در منوی Tools گزینه‌ی Preferences را کلیک کنید.<br />
۲-  در پنجره باز شده به سربرگ Advanced بروید.<br />
۳-  در لیست موجود در سمت چپ سربرگ باز شده، گزینه Content را کلیک کنید.<br />
۴-  در میان گزینه‌های جدید ظاهر شده، گزینه Enable Javascript را بیابید و اگر سمت چپ آن تیک نخورده، آن را تیک بزنید.<br />
۵-  بر روی کلید OK در پایین پنجره کلیک کنید تا تغییرات ثبت شوند.<br />
۶-  از Opera خارج شوید، و سپس دوباره آن را اجرا کنید.</p>


